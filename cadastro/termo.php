                <div class="modal-dialog modal-dialog-scrollable">
                    <h1>Objetivo:</h1>

                    <p>Este regulamento estabelece as condições sob as quais os clientes podem obter um café colonial mediante a apresentação de sua identidade.</p>

                    <h1>Elegibilidade:</h1>

                    <p>Para receber um café colonial, o cliente deve apresentar um documento de identidade válido que comprove sua identidade e sua idade (se necessário).</p>

                    <h1>Documentos Aceitáveis:</h1>
                    <p>
                        Os documentos aceitáveis para comprovação de identidade incluem, mas não se limitam a:
                        <br>
                        Carteira de Identidade (RG)
                        <br>
                        Carteira Nacional de Habilitação (CNH)
                        <br>
                        Passaporte
                        <br>
                        Carteira de Trabalho
                        <br>
                        Outros documentos oficiais com foto emitidos pelo governo.
                    </p>

                    <h1>Processo de Comprovação:</h1>
                    <p>
                        a) O cliente deve apresentar seu documento de identidade ao pessoal designado no estabelecimento.
                    <br>
                        b) O documento será verificado para garantir sua autenticidade e validade.
                    <br>
                        c) Se a identificação for considerada válida e o cliente atender aos requisitos, ele será elegível para receber o café colonial.
                    </p>

                    <h1>Elegibilidade por Idade:</h1>
                    <p>
                    Se houver restrições de idade para servir café colonial (por exemplo, bebidas alcoólicas incluídas), o documento de identidade também será verificado para confirmar a idade do cliente.
                    </p>


                    <h1>Disposições Gerais:</h1>
                    <p>
                        a) A comprovação de identidade é necessária para proteger a integridade do serviço oferecido.
                        <br>
                        b) O estabelecimento se reserva o direito de recusar o serviço caso a identificação não seja fornecida ou seja considerada inválida.
                        <br>
                        c) Este regulamento pode ser alterado ou atualizado a critério do estabelecimento.
                    </p>

                    <h1>Validade da Oferta:</h1>
                    <p>
                    Este regulamento se aplica durante o período em que o serviço de café colonial estiver sendo oferecido pelo estabelecimento.
                    </p>

                    <h1>Data de Vigência:</h1>
                    <p>Este regulamento entra em vigor a partir dentre 10 dias a data de seu aniversário.</p>
